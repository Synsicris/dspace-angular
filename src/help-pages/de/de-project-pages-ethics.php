      <h1>Ethische Reflexion</h1>
      <p>Im Monitoring-Tool nehmen Sie eine Reflexion der ethischen Aspekte aller - auch ausgelagerter - Schritte des Forschungs- und Entwicklungsprozesses (F&E-Prozess) sowie der Anwendung der Projektergebnisse vor.</p>
      <p>Durch die angemessene Berücksichtigung möglicherweise problematischer Effekte, Folgen und Risiken kann die gesellschaftliche Relevanz der Forschung erhöht werden. Die Forschung zeigt damit ihre Verantwortung für die Einhaltung ethischer Standards.</p>
      <p>Die ethische Reflexion führen Sie einmal für den gesamten Verbund durch.</p>

    <article>
		<h2>Fragen zur ethischen Reflexion </h2>

		<p><strong>1) Beteiligt die Forschung menschliche Teilnehmende?</strong></p>
    <p>Relevant bei jeder Arbeit mit Menschen als Forschungs- oder Studienteilnehmende, unabhängig von Art oder Thema der Forschungsarbeit. </p>
    <p>Beispiele:
      <ul>
        <li>Sammlung biologischer Proben oder persönlicher Daten</li>
        <li>medizinische Eingriffe</li>
        <li>Interviews</li>
        <li>Beobachtungen</li>
        <li>die sekundäre Nutzung oder Nachverfolgung von Informationen, die für andere Zwecke bereitgestellt wurden, wie z.B. für andere Forschungsprojekte, amtlich erhobene Daten oder Social-Media-Seiten</li>
      </ul>

</p>

      <details>
        <summary>
          Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
        </summary>
        <p>
          <ul>
            <li>In der Forschung müssen die Menschenwürde sowie die Rechte und Interessen der Beteiligten geschützt und ein angemessenes Verhältnis von Risiken zu Nutzen sichergestellt werden. Wie gewährleisten Sie den Schutz der Teilnehmenden?</li>
            <li>Welche Maßnahmen setzen Sie zum Schutz hilfsbedürftiger Menschen oder Schutzbefohlenen an?</li>
            <li>Ist es für Ihr Forschungsvorhaben erforderlich, dass Teilnehmende nicht vollständig über den Zweck der Studie informiert werden oder dass Teilnehmende getäuscht werden? Wie planen Sie in Ihrem Forschungsvorhaben mit den ethischen Implikation umzugehen?</li>
          </ul>
        </p>
      </details>


    <br>

    <p><strong>2) Beinhaltet die Forschung die Verarbeitung personenbezogener Daten?</strong></p>
    <p>Relevant bei Forschungsvorhaben, bei denen personenbezogene Daten verarbeitet werden, unabhängig von der verwendeten Methode (z. B. Interviews, Fragebögen, direkte Online-Abfrage). <strong>Personenbezogene Daten</strong> sind Informationen, die sich auf eine identifizierte oder identifizierbare natürliche Person beziehen. Dies schließt auch Fälle der indirekten Identifizierbarkeit mit ein (z.B. Fälle, in denen der Name der betroffenen Person nicht enthalten ist aber sich der Personenbezug durch Zusatzwissen verantwortlicher Personen ergeben kann).</p>

    <details>
      <summary>
        Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
      </summary>
      <p>
        <p>Gemäß Datenschutzgesetzgebung (DSGVO) müssen personenbezogene Daten nach bestimmten Grundsätzen und Bedingungen verarbeitet werden. Diese zielen darauf ab, negative Auswirkungen auf die betroffenen Personen zu begrenzen und Fairness, Transparenz und Rechenschaftspflicht bei der Datenverarbeitung sowie Datenqualität und Vertraulichkeit zu gewährleisten.

        <ul>
          <li>Wie stellen Sie die Einhaltung dieser Standards sicher? </li>
          <li>Welche personenbezogenen Daten werden im Rahmen des Forschungsprojekts erhoben?</li>
          <li>Wie gehen Sie mit den erhobenen Daten um? </li>
          <li>Welche Verfahren zur Anonymisierung und Pseudonymisierung verwenden Sie? </li>
          <li>Wie gewährleisten Sie die Sicherheit der erhobenen Daten vor externem Zugriff und missbräuchlicher Nutzung und Weitergabe? </li>
          <li>Werden die teilnehmenden Personen über die Datensicherheit und Datenverarbeitung informiert?</li>
        </ul>
      </p>
    </details>


  <br>


  <p>
    <p><strong>3) Beinhaltet Ihre Forschung die Verwendung von Materialien oder Stoffen, die Menschen Schaden zufügen können?</strong></p>
    <p>Relevant bei Forschungsvorhaben, bei denen durch verwendete Materialien oder Stoffe für Versuchspersonen, Forschende oder unbeteiligte Dritte <strong>Risiken für Gesundheit und Leben </strong>entstehen (z. B. durch biologisch gefährliche Materialien, giftige Chemikalien, Sprengstoffe oder radioaktive Stoffe).</p>

    <details>
      <summary>
          Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
      </summary>
      <p>Die Gesundheit und der Lebensschutz aller menschlichen Teilnehmenden müssen bei allen Forschungsstudien hohe Priorität haben.</p>

      <ul>
        <li>Welche Risiken bestehen in Ihrem Forschungsvorhaben?</li>
        <li>Welche entsprechenden Vorsorgemaßnahmen ergreifen Sie zum Schutz der Beteiligten?</li>
      </ul>

     </p>
    </details>


  <br>




     <p><strong>4) Umfasst die Forschung Tierversuche?</strong></p>
     <p>Relevant bei der Verwendung von Tieren in den verschiedensten Formen von Experimenten, Verfahren oder Techniken.</p>


     <details>
       <summary>
           Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
       </summary>
       <p>Sie sind verpflichtet, das 3R-Prinzip umzusetzen. Ziel des 3R Prinzips ist es, Tierversuche vollständig zu vermeiden (<strong>Replacement</strong>) sowie die Zahl der Tiere (<strong>Reduction</strong>) und mögliches Leiden (<strong>Refinement</strong>) in Versuchen auf das unerlässliche Maß zu beschränken.</p>

       <ul>
         <li>An welchen Tierarten und wie vielen Tieren haben Sie Tierversuche geplant? Welche Schmerzen, Leiden oder Schäden könnten die Tiere durch die Versuche erleiden?</li>
         <li>Warum lässt sich das Forschungsziel nicht ohne Tierversuche erreichen? </li>
         <li>Warum ist die geplante Anzahl der verwendeten Tiere erforderlich? Ließe sich die geplante Zahl an Versuchstieren weiter reduzieren? </li>
         <li>Wie tragen Sie dafür Sorge, mögliche Leiden der Versuchstiere zu minimieren oder zu vermeiden?</li>
       </ul>

      </p>
     </details>


     <br>


    <p><strong>5) Können die Forschungsarbeiten der Umwelt, Tieren oder Pflanzen schaden?</strong></p>
    <p>Relevant bei Forschungsvorhaben mit Materialien oder Stoffen, die einen nachteiligen Einfluss auf die Umwelt, Tiere, Pflanzen oder die biologische Vielfalt haben (wie z. B. gefährliche Abfallstoffe und giftige Chemikalien) oder Forschungsaktivitäten, die negative Auswirkungen auf gefährdete Fauna/Flora oder Schutzgebiete haben können.</p>


    <details>
      <summary>
        Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
      </summary>
      <p>Genehmigungen von Umweltbehörden müssen, falls erforderlich, eingeholt werden. Es gilt grundsätzlich das <strong>Vorsorgeprinzip</strong>: auch bei unvollständigem oder unsicherem Wissen über Art, Ausmaß, Wahrscheinlichkeit sowie Kausalität von Umweltschäden und -gefahren muss vorbeugend gehandelt werden, um Schäden von vornherein zu vermeiden. </p>

      <ul>
        <li>Welche Risiken für die Umwelt bestehen?</li>
        <li>Warum ist das Eingehen dieser Risiken für das Erreichen Ihres Forschungsvorhabens unerlässlich?</li>
        <li>Welche entsprechenden Vorsorgemaßnahmen ergreifen Sie zur Verringerung bzw. Vermeidung dieser Risiken?</li>
      </ul>

     </p>

    </details>


    <br>




   <p><strong>6) Forschung mit menschlichen Zellen oder Geweben</strong></p>
   <p>Relevant für Forschungsprojekte, in denen menschliche Zellen oder Gewebe (inklusive menschlicher embyonaler oder fetaler Zellen und Gewebe, die keine Stammzellen sind) produziert, genutzt oder gewonnen werden. Diese Zellen oder Gewebe können von kommerziellen Quellen, aus dem eigenen Projekt, von einem anderen Forschungsprojekt, aus einem Labor oder einer Biomaterialbank stammen.</p>


   <details>
     <summary>
       Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
     </summary>
     <p>Für diese Forschung sind zwingend <strong>gesetzlich geregelte Ethikbewertungen</strong> erforderlich. Wenden Sie sich an die entsprechenden Expert:innen in Ihrer Einrichtung oder übergeordneter Stellen. Erfolgte Gutachten können Sie auf der Eingabeseite zur ethischen Reflexion hochladen.</p>

     <ul>
       <li>Wie stellen Sie sicher, dass Spender:innen umfänglich über die primäre und sekundäre Nutzung des entnommenen Materials informiert werden und Zellen nur bei vollem Einverständnis entnommen werden?</li>
       <li>Wie stellen Sie sicher, dass internationales, europäisches und nationales Recht (insbesondere die EU-Verordnung 2004/23) zum Umgang mit menschlichen Zellen und Geweben eingehalten werden?</li>
       <li>Wie werden Informationen über Ursprung, Lagerdauer, Menge und Prozeduren der erzeugten, genutzten oder gesammelten Zellen gesichert?</li>
     </ul>

    </p>

   </details>


   <br>



  <p><strong>7) Forschung mit menschlichen embryonalen Stammzellen</strong></p>
  <p>Relevant bei allen Forschungsaktivitäten mit embryonalen Stammzellen oder menschlichen Embryonen.</p>



  <details>
    <summary>
      Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
    </summary>

      <p>Für diese Forschung sind zwingend <strong>gesetzlich geregelte Ethikbewertungen</strong> erforderlich. Außerdem bedarf es einer wissenschaftlichen <strong>Evaluation über die Notwendigkeit</strong> der Nutzung embryonaler Stammzellen für das Forschungsziel. Wenden Sie sich an die entsprechenden Expert:innen in Ihrer Einrichtung oder übergeordneter Stellen. Erfolgte Gutachten können Sie auf der Eingabeseite zur ethischen Reflexion hochladen.</p>

      <ul>
        <li>Wie stellen Sie sicher, dass Zellen nur bei vollem und informiertem Einverständnis der Spender:innen entnommen werden?</li>
        <li>Wie stellen Sie sicher, dass internationales, europäisches und nationales Recht (insbesondere die Kommissions-Entscheidung zu Forschung mit Embryonen und embryonalen Stammzellen: Artikel 11(4) HE Specific Programme Decision 2021/764) zur Forschung mit menschlichen embryonalen Stammzellen eingehalten werden?</li>
        <li>Wie werden Informationen über Ursprung, Lagerdauer, Menge und Prozeduren der Stammzellen bzw. Embryonen gesichert?</li>
      </ul>

     </p>

  </details>


  <br>





 <p><strong>8) Berühren die Forschungs- und Entwicklungsergebnisse des Projektes Grenzbereiche der ethischen Akzeptanz? </strong></p>
 <p>Relevant, wenn zu Ihrer Forschung und den Ergebnissen gesellschaftliche Kontroversen bestehen, die ethische Aspekte zu Mensch, Tier oder Umwelt beinhalten (z. B. Diskurse in den Medien, Demonstrationen, Stellungnahmen).</p>

 <details>
   <summary>
     Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
   </summary>
   <p>Für Forschung, die von einer kritischen Auseinandersetzung in der Gesellschaft begleitet wird, ist gefordert, sowohl mit Kritik als auch mit Kritiker:innen angemessen umzugehen.</p>

   <ul>
     <li>Beabsichtigen Sie, Diskussionen über ethische Gesichtspunkte Ihrer Projektergebnisse anzuregen oder sich an solchen zu beteiligen?</li>
     <li>Wie stellen Sie sicher, dass auf ethisch relevante Kritik und Bedenken bezüglich des Forschungsvorhabens angemessene Reaktionen folgen?</li>
   </ul>

  </p>


 </details>


 <br>


<p><strong>9) Betrifft die Forschung Dual-Use-Güter bzw. genehmigungspflichtige Güter?</strong></p>
<p>Relevant für Forschungsprojekte zu Gütern, die sowohl für zivile als auch für militärische Zwecke (<strong>Dual-Use-Güter</strong>) verwendet werden können, oder anderen Gütern, für die eine Genehmigung erforderlich ist. Letzteres gilt beispielsweise für Forschungen im Zusammenhang mit Krankheitserregern sowie für die Entwicklung autonomer Robotik, Drohnen und bestimmter Lasertechnologien.</p>

<details>
  <summary>
  Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
  </summary>

  <p>In vielen Fällen außerhalb der Sicherheitsbranche sind Forschende wenig vertraut mit Sicherheitsvorkehrungen und -bestimmungen. Konsultieren Sie bei Bedarf Expert:innen (in Ihrer Einrichtung), die mit Sicherheitsethik vertraut sind, und machen Sie sich mit den Empfehlungen
  von DFG und Leopoldina zum Umgang mit sicherheitsrelevanter Forschung vertraut.
  </p>

  <ul>
    <li>Welche Güter und Informationen, die in Ihrer Forschung verwendet und produziert werden, benötigen Ausfuhrgenehmigungen?</li>
    <li>Wie stellen Sie sicher, dass Ausfuhrgenehmigungen und weitere außenwirtschaftsrechtliche Vorschriften (Kriegswaffenkontrollgesetz usw.) eingehalten werden?</li>
    <li>Wie ist das Risiko/Nutzen-Verhältnis einzuschätzen?</li>
    <li>Welche Maßnahmen zur Risikominimierung haben Sie geplant?</li>
  </ul>

  </p>


</details>

<br>


<p><strong>10) Besteht ein Potenzial für den Missbrauch von Forschungsergebnissen?</strong></p>
<p>Relevant, wenn die beteiligten oder erzeugten Materialien, Methoden oder Technologien und das vorhandene bzw. generierte Wissen Menschen, Tieren oder Umwelt schaden können, wenn die Forschungsergebnisse z. B. modifiziert werden, in die falschen Hände gelangen oder anderen als den vorgesehenen Zwecken dienen könnten und diese Verwendung unethisch wäre.</p>

<details>
  <summary>
  Leitfragen zur Beschreibung der Vorgehensweise in Ihrem Forschungsvorhaben:
  </summary>


  <p>Führen Sie eine Bewertung durch, in der Sie die Folgen, Nebenfolgen und Risiken eines Missbrauchs Ihrer Forschungsergebnisse einschätzen und die Maßnahmen darstellen, mit denen Sie einen Missbrauch vermeiden.</p>

  <ul>
    <li>Wie ist die Gefahr für einen Missbrauch der Forschungsergebnisse des Vorhabens einzuschätzen und welche Risiken könnten durch einen Missbrauch der Forschungsergebnisse entstehen?</li>
    <li>Welche Maßnahmen zur Minimierung dieser Risiken haben Sie geplant? (Änderung des Projektdesigns z.B. durch Nutzung von Dummy-Daten, Einschränkung der Verbreitung der Ergebnisse usw.)</li>
    </ul>

  </p>

</details>

<br>




<p><strong>11) Gibt es andere zu berücksichtigende ethische Aspekte hinsichtlich der Forschungs- und Entwicklungsprozesse oder einer zukünftigen Verwendung der Projektergebnisse? </strong></p>
<p>Relevant, wenn in Bezug auf das Projekt neue oder andere ethische Themen und Bedenken auftreten, die mit den oben genannten Punkten nicht abgedeckt sind.</p>


