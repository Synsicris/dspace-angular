    <h2>Vielfältige Nutzbarkeit der Informationen</h2>
    <p>Durch die strukturierte Erfassung lassen sich die Informationen für verschiedene Zwecke nutzen und in vielfältiger Weise für diese Zwecke aufbereiten.</p>

    <br>

        <h3> Projektmanagement und Kommunikation mit dem Fördermittelgeber</h3>
          <p><strong>Projektkonsortien</strong> erarbeiten im Monitoring-Tool für den Antrag eine gemeinsam abgestimmte <strong>Informationsgrundlage für den Fördermittelgeber</strong> und ersetzen damit die Vorhabensbeschreibung. Das Tool bietet dabei Unterstützung für die Arbeits- und Wirkungsplanung.
            Für den <strong>Fördermittelgeber</strong> wird eine Version mit Leserechten erstellt.
            Das Projektkonsortium arbeitet in seiner Version weiter und aktualisiert die Angaben kontinuierlich. Die Aktualisierung wird durch Schnittstellen unterstützt, aus denen Projektdaten (profi des Fördermittelgebers), Publikationsdaten und Patentinformationen importiert werden können.</p>
            <p>Für Zwischenberichte oder zu anderen gewünschten Zeitpunkten kann dem Fördermittelgeber wieder eine aktuelle <strong>Version mit Leserechten</strong> erstellt werden.
            Die Versionen können miteinander verglichen werden, um Veränderungen im (Teil-)Projekt zu darzustellen.</p>
            <p>Projektkonsortien und die beteiligten Forschenden haben damit jederzeit die Dokumentation und den Nachweis der Projektarbeit sowie möglicher Wirkungen des (Teil-)Projektes <strong>in verschiedenen Darstellungsformen verfügbar</strong>.
            Fördermittelgeber erhalten eine Dokumentation zum (Teil-)Projekt, die sich für die Projektbetreuung, wirkungsorientierte Förderprozesse und weitere Aufgaben vielfältig nutzen lässt.
          </p>

    <br>

        <h3> Programmsteuerung und –evaluierung </h3>
          <p>Informationen können projektübergreifend für vielfältige Aufgaben der Fördermittelgeber ausgewertet werden.</p>

    <br>

        <h3>Transfer in Praxis und Öffentlichkeit</h3>
        <p>Projektkonsortien können einstellen, welche Informationen öffentlich zugänglich werden. Damit besteht eine <strong>gut durchsuchbare Informationsgrundlage zu Materialien und Inhalten</strong> aus anwendungsorientierten Forschungsprojekten, die auch über den Bestand von projektspezifischen Homepages hinausgeht.</p>

    <br>

        <h3>Ex-post Projektevaluierung</h3>
        <p>Die im Monitoring-Tool erfassten Daten und Informationen können für die Begutachtung anhand des multidimensionalen Kriterien-Sets aufbereitet werden. Damit besteht eine Grundlage für die <strong>Bewertung der Beiträge von Forschung zu einer gesellschaftlichen Wirkung</strong>. Damit kann die Anerkennung dieser Beiträge von Forschungsprojekten wie auch die wirkungsorientierten Programmevaluierung unterstützt werden.</p>


