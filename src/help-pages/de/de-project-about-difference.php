		<h2>Das Monitoring-Tool ersetzt Teile von Anträgen und Berichten</h2>
		<p>Statt Fließtext in einem Textdokument zu einem bestimmten Gliederungspunkt der Vorhabensbeschreibung zu schreiben, zerlegen Sie <strong>die Informationen zum (Teil-)Projekt in einzelne Einträge</strong>.</p>
    <p>Jeder Eintrag gehört zu einer bestimmten Kategorie (z.B. Arbeitspaket oder Veranstaltung). Über die Einträge in den verschiedenen Kategorien stellen Sie <strong> Hintergründe, Ziele, Arbeiten, Ergebnisse und Wirkungen des (Teil-)Projektes </strong>dar. Jeder Eintrag hat eine Bezeichnung und ein Beschreibungsfeld und je nach Kategorie des Eintrags werden weitere Informationen erfragt.</p>
    <p>Im Tool finden Sie einen Arbeitsplan, den Wirkungspfad und den Verwertungsplan, in denen Sie die einzelnen Einträge wie an einer Pinnwand an- und abpinnen können, um sie zu strukturieren. Durch diese Strukturierung des Tools können Sie jederzeit von den Übersichten in die Detailseiten der Einträge wechseln.</p>



