    <h1>Verwertungsrechte und -pflichten?</h1>
      <ul>
      <li>Bei einer öffentlichen Förderung eines Projektes bleiben Sie (als Zuwendungsempfänger) Urheber der Projektergebnisse und besitzen das Recht auf die Nutzung der Projektergebnisse, haben jedoch gegebenenfalls auch eine Verwertungspflicht.</li>
      <li>Die konkreten rechtlichen Bedingungen unterscheiden sich zwischen den einzelnen Förderlinien. Beachten Sie deshalb die exakten rechtlichen Vorgaben in der Bekanntmachung und im Zuwendungsbescheid.</li>
          <ul>
            <li>Beispiel für den <strong>Fokus auf die wirtschaftliche Förderung</strong>:<br> Wenn Ihnen alleinige Nutzungsrechte an den Projektergebnissen zugesprochen werden, ist die Anmeldung von Schutzrechten (z.B. ein Patent für eine Erfindung) eine wichtige Maßnahme, um diese gegenüber anderen Unternehmen abzusichern. Der Fördermittelgeber kann verbunden mit den Nutzungsrechten auch eine Verwertungspflicht festlegen, d.h. Sie sind verpflichtet die Ergebnisse in der im Verwertungsplan beschriebenen Weise zu nutzen. Falls Sie die Ergebnisse nicht verwerten, kann das im schlimmsten Fall dazu führen, dass Ihnen das alleinige (exklusive) Nutzungsrecht entzogen oder sogar die Zuwendung zurückgefordert wird.</li>
            <li>Beispiel für den <strong>Fokus auf eine breite gesellschaftliche Nutzung</strong> der Ergebnisse:<br> Wenn eine Veröffentlichungspflicht besteht, haben Sie keine ausschließlichen, sondern nur eingeschränkte Nutzungsrechte. Dann könnte eine Anmeldung von Schutzrechten sogar kritisch gesehen werden. Es wird vielmehr erwartet, dass Sie im Verwertungsplan beschreiben, wie die Ergebnisse für Dritte optimal zugänglich und nutzbar gemacht werden und wie Sie Zielgruppen aktiv erreichen wollen.</li>
          </ul>
      <li>Deshalb ist es wichtig, dass die projektbeteiligten Einrichtungen im Verwertungsplan darlegen, wie sie Ergebnisse verwerten möchten, ob Schutzrechte für die ausschließliche Nutzung angemeldet werden sollen oder ob und wie die Projektergebnisse frei zugänglich gemacht und verbreitet werden.</li>
      <li>Grundsätzlich gibt es somit in jedem Projekt den Auftrag, Verwertung zu betreiben, d.h. die Projektergebnisse zu nutzen oder für Dritte nutzbar zu machen. Dies soll mit möglichst konkreten und realisierbaren Ziele, Ergebnissen und Umsetzungsschritten im Verwertungsplan beschrieben werden.</li>
    </ul>


    </ul>

