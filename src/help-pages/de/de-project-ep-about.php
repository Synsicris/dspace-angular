		<h1>Was ist eigentlich Verwertung?</h1>
		<p>Verwertung ist die Nutzung der Projektergebnisse. Da Sie als Zuwendungsempfänger öffentliche Fördermittel erhalten, soll nicht „nur“ geforscht werden, sondern Ergebnisse sollen auch genutzt werden.</p>
        <p>Eine <strong>Verwertung</strong> kann sowohl <strong>durch Sie als Zuwendungsempfänger</strong> als auch durch die Verwertungsmöglichkeiten, die für <strong>Dritte</strong> bestehen, erfolgen.</p>
        Mit der Nutzung der Projektergebnisse sollen Vorteile entstehen, die zum Erreichen der jeweiligen Förderziele beitragen (z.B. Steigerung der Wettbewerbsfähigkeit von KMU, verbesserter Umweltschutz etc.).
    </p>
<br>

    <h1>Warum muss ein Verwertungsplan erstellt werden?</h1>
    <p>Grundsätzlich sollen im Verwertungsplan Nutzungsabsichten und –möglichkeiten für die Projektergebnisse sowie damit erzielte Vorteile beschrieben werden.
      Das dient verschiedenen Zielen:
        <ul>
          <li>Die mögliche Verwertung soll von Ihnen durchdacht und beschrieben werden, damit bereits im Projektverlauf Weichenstellungen und Aktivitäten für eine spätere Nutzung der Projektergebnisse erfolgen können.</li>
          <li>Die Förderwürdigkeit des Vorhabens wird u.a. daran geprüft, ob die geplante Verwertung zu den Förderzielen beiträgt.</li>
          <li>Ihre spezifischen Verwertungsrechte und -pflichten können vom Projektträger festgelegt und geprüft werden. Deshalb muss der Verwertungsplan auch von jedem Zuwendungsempfänger / Teilprojekt separat erstellt werden.</li>
        </ul>
      </p>

<br>
