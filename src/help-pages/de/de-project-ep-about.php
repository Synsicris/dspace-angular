    <h1>Verwertungsplan – von der Skizze bis nach dem Projektende</h1>
    <ul>
    		<li>Erste Angaben zur Verwertung haben Sie bereits in der Skizze gemacht.</li>
    		<li>Im <strong>Antrag</strong> konkretisieren Sie weiter, <strong>wie die angestrebten Ergebnisse und Erkenntnisse zukünftig (nach Projektende) verwertet werden können</strong>.</li>
        <li>Der Verwertungsplan ist <strong>jedes Jahr</strong> mit dem Zwischenbericht <strong>zu aktualisieren (fortzuschreiben)</strong>. Das bedeutet: Sie arbeiten in den bestehenden Verwertungsplan <strong>Änderungen</strong> ein, die sich aus den im Projekt gewonnenen Erkenntnissen oder Erfahrungen ableiten (z.B. weil eine Testvariante bessere Effekte erzielt oder Grenzen in der Umsetzbarkeit identifiziert wurden). Sie <strong>konkretisieren</strong> jedes Jahr die geplante Verwertung und Sie <strong>ergänzen</strong> neue Verwertungsmöglichkeiten, die im Projektverlauf identifiziert wurden. Dadurch wird der Verwertungsplan im Projektverlauf immer detaillierter und präziser.</li>
      	<li>Der letzte Verwertungsplan erfolgt (je nach Vorgabe des Fördermittelgebers) mit dem <strong>Schlussbericht oder 2 Jahre nach Projektende</strong>.</li>
        <li>Jeder Verwertungsplan kann jederzeit im Monitoring-Tool verändert werden. Wenn Sie eine Version für den Fördermittelgeber freigeben, wird diese Version schreibgeschützt archiviert. Angaben aus vorangegangenen Jahren sind folglich weiterhin abrufbar und gehen nicht verloren.</li>
    </ul>

