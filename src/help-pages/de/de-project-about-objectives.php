      <h2>Wirkungsverständnis und Ziele für die Entwicklung des Kriterien-Sets und des Monitoring-Tools</h2>
<p>	Die folgenden Aspekte waren maßgeblich für die Entwicklung des Kriterien-Sets und des Monitoring-Tools:
      <ul>
          <li>Grenzen der Wirkungsmessung berücksichtigen:</li>
              <ul>
                  <li>Wirkung entsteht in komplexen Innovationssystemen, in denen Forschung einen Beitrag zur Entwicklung von Wirkungen leistet.</li>
                  <li>Gesellschaftliche Wirkung entsteht erst mit erheblichem Zeitverzug und lässt sich nicht einzelnen Forschungsvorhaben zuordnen</li>
                  <li>Deshalb liegt der Fokus nicht auf der Wirkungsmessung, sondern auf den Beiträgen der Forschung für die Entwicklung von Wirkungen, z.B. in Form von Interaktionen, die Wirkungen wahrscheinlicher machen. </li>
              </ul>
          <li>Forschung fair und transparent bewerten:</li>
              <ul>
                  <li>Um Teamerfolg zu honorieren, wird ein Verbundprojekt als besonders geeignet angesehen, um Beiträge der Forschung zu erfassen.</li>
                  <li>Projekte sind zudem ein zeitlich und inhaltlich gut abgegrenzter Bewertungsgegenstand.</li>
              </ul>
          <li>Eignung für verschiedene Förderprogramme, Projekttypen, Innovationen und Transferstrategien erzielen: dafür wurden zahlreiche fachliche Hintergründe integriert, wie Wirkungen wahrscheinlicher werden.</li>


      </ul>
  </p>
