		<h2>Wozu dient das Monitoring-Tool?</h2>

		<p>Das <strong>Monitoring-Tool</strong> wurde entwickelt, um Beiträge der Forschung zur Lösung gesellschaftlicher Herausforderungen zu erfassen und bewerten zu können.
Mit anderen Worten: das Engagement der Forschung für die Entwicklung und den Transfer von nachhaltigen Lösungen soll erfasst und gefördert werden.
Um dieses Engagement honorieren zu können, wurde ein<strong> Kriterien-Set</strong> und ein <strong>Konzept für die Evaluierung von Projekten nach Projektende</strong> (= Ex-post Projektevaluierung) entwickelt.
</p>
Beide Elemente sollen innerhalb von Forschungsförderprozessen eingesetzt werden, weil:

<ul>
<li>dadurch die Informationen ohne zusätzlichen Aufwand für die Forschenden erfasst werden können, indem Teile von
  Antragstexten und Berichten durch die Dokumentation im Monitoring-Tool ersetzt werden.</li>
<li>die Informationen für die Projektbegleitung und das Programmanagement der Fördermittelgeber sehr nützlich sind.</li>
<li>eine Projektevaluierung in diesem Kontext sowohl der Honorierung von Leistungen der Forschenden als auch als Grundlage für
  wirkungsorientierte Programmevaluierungen dienen kann.  </li>
</ul>


